a=int(input("Enter the first number:"))
o=input("Entetr operator: +,-,*,/,%:")
b=int(input("Enter the second number:"))
if o=="+":
    print(a+b)
elif o=="-":
    print(a-b)
elif o=="*":
    print(a*b)
elif o=="/":
    print(a/b)
elif o=="%":
    print(a%b)
else:
    print("Go and learn maths first")
